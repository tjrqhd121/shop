package com.inhatc.vo;

public class RegisterVO {
	private String id;
	private String pw;
	private String email;
	private String name;
	private String phone;
	private String postadd;
	private String roadadd;
	private String jibadd;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getRoadadd() {
		return roadadd;
	}
	public void setRoadadd(String roadadd) {
		this.roadadd = roadadd;
	}
	public String getPostadd() {
		return postadd;
	}
	public void setPostadd(String postadd) {
		this.postadd = postadd;
	}
	public String getJibadd() {
		return jibadd;
	}
	public void setJibadd(String jibadd) {
		this.jibadd = jibadd;
	}
}